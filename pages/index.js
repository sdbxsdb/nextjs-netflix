import Head from "next/head";
import Banner from "../components/banner/banner";
import NavBar from "../components/nav/navbar";
import SectionCards from "../components/card/section-cards";
import {
  getPopularVideos,
  getVideos,
  getWatchItAgainVideos,
} from "../lib/videos";
import redirectUser from "../utils/redirectUser";

export async function getServerSideProps(context) {
  const { userId, token } = await redirectUser(context);

  if (!userId) {
    return {
      props: {},
      redirect: {
        destination: '/login',
        permanent: false,
      }
    }
  }

  const watchItAgainVideos = await getWatchItAgainVideos(userId, token);

  const disneyVideos = await getVideos("disney trailer");
  const comedyVideos = await getVideos("comdey movies");
  const codingVideos = await getVideos("coding");
  const popularVideos = await getPopularVideos();


  return {
    props: JSON.parse(
      JSON.stringify({
        disneyVideos,
        comedyVideos,
        codingVideos,
        popularVideos,
        watchItAgainVideos,
      })
    ),
  };
}

export default function Home({
  disneyVideos,
  comedyVideos,
  codingVideos,
  popularVideos,
  watchItAgainVideos = [],
}) {


  return (
    <div>
      <Head>
        <title>Netflix | SDB</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <NavBar />

      <div className="mb-16">
        <Banner
          videoId="TcMBFSGVi1c"
          title="A movie name"
          subtitle="A movie subtitle"
          imgUrl="/static/avengerslandscape.jpg"
        />
      </div>

      <SectionCards title="Disney" videos={disneyVideos} size="portrait" />
      <SectionCards
        title="Watch it again"
        videos={watchItAgainVideos}
        size="landscape"
      />
      <SectionCards title="Comedy" videos={comedyVideos} size="landscape" />
      <SectionCards title="Learning" videos={codingVideos} size="square" />
      <SectionCards title="Popular" videos={popularVideos} size="landscape" />
    </div>
  );
}
